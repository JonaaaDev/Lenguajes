# guardar booleans (verdadero o falso)

# las variables de tipo «bool» solo pueden tener dos estados: True o False
# son como interruptores: activo/desactivo, sí/no, abierto/cerrado

# ejemplo de estado de un videojuego
esta_vivo = True
partida_pausada = False

# ejemplo de permisos de usuario
tiene_premium = True
es_administrador = False

# ¡Recordad!: la primera letra DEBE ser mayúscula (True / False)
# Si lo escribes en minúscula (true), Python te dará un error
# Si lo pones entre comillas ("True"), dejará de ser un bool y será un «str»

# estas variables son muy útiles para cambiar el flujo del programa
# por ejemplo, si el jugador toca un pincho:
esta_vivo = False
